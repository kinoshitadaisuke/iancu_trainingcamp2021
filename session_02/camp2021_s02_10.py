# importing math module
import math

# calculation of square root of 2
a = 2.0
b = math.sqrt (a)

# printing result
print ("square root of %15.13f = %15.13f" % (a, b) )

# calculation of log (20)
c = 20.0
d = math.log (c)

# printing result
print ("log (%f) = %f" % (c, d) )

# calculation of log10 (1000)
e = 1000.0
f = math.log10 (e)

# printing result
print ("log10 (%f) = %f" % (e, f) )

# calculation of exp (3)
g = 3.0
h = math.exp (g)

# printing result
print ("exp (%f) = %f" % (g, h) )
